/**
 * 결과 세포의 오늘 운세 한 줄. 날짜해시로 결정론적(같은 날 = 같은 운세).
 * date 주입 가능(테스트용). 기본은 오늘.
 */
export function getFortune(cell: ScoredCellId, date: Date = new Date()): string {
  const ymd =
    date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate();
  const list = FORTUNES[cell];
  return list[ymd % list.length];
}
